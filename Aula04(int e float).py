'''
Tipos int e float

int = Números inteiros
O tipo int representa qualquer número positivo ou negativo
Int sem sinal sempre será posivito
'''
print("Ints")
print(11) #int
print(-11) #int
print(0, end="\n\n") #int

'''
float = número com pontos flutuantes
O tipo float representar qualquer número positivo ou negativo
que tenha um ponto flutuante
float sem sinal é sempre positivo
'''

print("Floats")
print(1.1) #float
print(-1.1) #float
print(0.0, end='\n\n') #float

#A função type mostra o tipo de dado que o python inferiu ao valor
print("type function")
print(type("Rita de Cássia")) # String
print(type(0.0)) # float 0.0
print(type(0)) # int 0