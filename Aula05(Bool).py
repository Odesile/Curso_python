'''
Tipo de dado bool (boolean)
Ao questionar algo em um programa
só existem duas repostas possíveis
sim (True) ou não (False).
Existem vário operadores para "questionar"
Dentre eles o '==', que é um operador lógico que questiona
um valor, se é igual a outro
'''

#Verificando  datatype
print(True , type(True)) # sim - Boolean
print(False , type(False)) # não - Boolean

#Utilizando operadores comparativos
print("10 = 10", 10 == 10)
print("10 = 11", 10 == 11)
